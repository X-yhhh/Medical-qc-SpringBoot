// 遍历图标映射表并注册为全局组件，避免页面逐个导入。
export const registerGlobalIcons = (app) => {
  for (const [name, component] of Object.entries(GLOBAL_ICONS)) {
    // 注册后可在任意模板中直接用 <House /> 这类标签引用。
    app.component(name, component)
  }
}
